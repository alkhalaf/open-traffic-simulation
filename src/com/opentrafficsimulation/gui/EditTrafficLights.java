    private void jButtonShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonShowActionPerformed

        if (jListLights.getSelectedIndex() >= 0) {
            String id = String.valueOf(jListLights.getSelectedValue());
            System.out.println("Selected Light: " + id);

            try {
                TraciConnector.GOTO_JUNCTION = id;
            } catch (Exception exception) {
                JOptionPane.showMessageDialog(null, "Could not connect to Traci simulation");
                exception.printStackTrace();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please select a traffic list from the list!");
        }
    }//GEN-LAST:event_jButtonShowActionPerformed

