    public void openEditor(String id) {
        try {
            String state = TraciConnector.RTL.readTLState(id);
            jLabelState.setText(state);
            jPanelEdit.setVisible(true);
            jButtonEdit.setEnabled(false);
            jListLights.setEnabled(false);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }


